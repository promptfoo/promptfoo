async function executeMetaTargetAttempt({
  iteration,
  numIterations,
  prompt,
  updatedVars,
  filters,
  targetProvider,
  injectVar,
  iterationContext,
  options,
  context,
  tracingOptions,
  shouldFetchTrace,
  traceSnapshots,
  totalTokenUsage,
}: {
  iteration: number;
  numIterations: number;
  prompt: Prompt;
  updatedVars: Record<string, VarValue>;
  filters: NunjucksFilterMap | undefined;
  targetProvider: ApiProvider;
  injectVar: string;
  iterationContext: Awaited<ReturnType<typeof createIterationContext>>;
  options?: CallApiOptionsParams;
  context?: CallApiContextParams;
  tracingOptions: ReturnType<typeof resolveTracingOptions>;
  shouldFetchTrace: boolean;
  traceSnapshots: TraceContextData[];
  totalTokenUsage: TokenUsage;
}): Promise<{
  targetResponse: TargetResponse;
  traceContext: TraceContextData | null;
  computedTraceSummary?: string;
} | null> {
  const targetPrompt = await renderPrompt(prompt, updatedVars, filters, targetProvider, [injectVar]);
  logger.debug('[IterativeMeta] Calling target with agent-generated prompt', {
    iteration,
    promptLength: String(updatedVars[injectVar] ?? '').length,
  });

  const iterationStart = Date.now();
  const targetContext = buildIterationTargetContext(iterationContext, updatedVars);
  const initialTargetResponse: TargetResponse = await getTargetResponse(
    targetProvider,
    targetPrompt,
    targetContext,
    options,
  );
  const targetResponse: TargetResponse = await externalizeResponseForRedteamHistory(
    initialTargetResponse,
    {
      evalId: context?.evaluationId,
      testIdx: context?.testIdx,
      promptIdx: context?.promptIdx,
    },
  );
  accumulateResponseTokenUsage(totalTokenUsage, targetResponse);

  const { traceContext, computedTraceSummary } = await fetchIterativeMetaTrace({
    context,
    tracingOptions,
    shouldFetchTrace,
    iterationStart,
    traceSnapshots,
  });
  logger.debug('[IterativeMeta] Raw target response', {
    responseLength: targetResponse.output?.length,
    hasTrace: !!traceContext,
  });

  if (targetResponse.error) {
    logger.info(`[IterativeMeta] ${iteration}/${numIterations} - Target error`, {
      error: targetResponse.error,
    });
    return null;
  }
  if (!Object.prototype.hasOwnProperty.call(targetResponse, 'output')) {
    logger.info(
      `[IterativeMeta] ${iteration}/${numIterations} - Malformed target response - missing output property`,
    );
    return null;
  }

  return { targetResponse, traceContext, computedTraceSummary };
}

async function gradeIterativeMetaResponse({
  iteration,
  attackPrompt,
  targetResponse,
  test,
  iterationVars,
  gradingProvider,
  additionalRubric,
  tracingOptions,
  traceContext,
  computedTraceSummary,
  lastTransformResult,
  context,
}: {
  iteration: number;
  attackPrompt: string;
  targetResponse: TargetResponse;
  test?: AtomicTestCase;
  iterationVars: Record<string, VarValue>;
  gradingProvider: ApiProvider;
  additionalRubric: string;
  tracingOptions: ReturnType<typeof resolveTracingOptions>;
  traceContext: TraceContextData | null;
  computedTraceSummary?: string;
  lastTransformResult?: TransformResult;
  context?: CallApiContextParams;
}): Promise<GradingResult | undefined> {
  let assertToUse = test?.assert?.find(
    (a: { type: string }) => a.type && a.type.includes(test.metadata?.pluginId),
  );
  if (!assertToUse) {
    assertToUse = test?.assert?.find((a: { type: string }) => a.type);
  }
  if (!test || !assertToUse) {
    return undefined;
  }

  const { getGraderById } = await import('../graders');
  const grader = getGraderById(assertToUse.type);
  if (!grader) {
    return undefined;
  }

  const iterationTest = { ...test, vars: iterationVars };
  const gradingContext: RedteamGradingContext = {
    providerResponse: targetResponse,
    ...(tracingOptions.includeInGrading
      ? { traceContext, traceSummary: computedTraceSummary }
      : {}),
  };

  const webPageUuid = lastTransformResult?.metadata?.webPageUuid as string | undefined;
  if (webPageUuid) {
    const webPageUrl = lastTransformResult?.metadata?.webPageUrl as string | undefined;
    const evalId =
      context?.evaluationId ??
      (webPageUrl?.match(/\/dynamic-pages\/([^/]+)\//)?.[1] as string | undefined);
    logger.debug('[IterativeMeta] Fetching exfil tracking from server API', {
      webPageUuid,
      evalId,
      source: 'lastTransformResult.metadata',
    });
    try {
      const exfilData = await checkExfilTracking(webPageUuid, evalId);
      if (exfilData) {
        Object.assign(gradingContext, {
          wasExfiltrated: exfilData.wasExfiltrated,
          exfilCount: exfilData.exfilCount,
          exfilRecords: exfilData.exfilRecords,
        });
      }
    } catch (error) {
      logger.warn('[IterativeMeta] Failed to fetch exfil tracking from server', {
        error,
        webPageUuid,
      });
    }
  }

  if (
    gradingContext.wasExfiltrated === undefined &&
    targetResponse.metadata?.wasExfiltrated !== undefined
  ) {
    logger.debug('[IterativeMeta] Using exfil data from provider response metadata (fallback)');
    Object.assign(gradingContext, {
      wasExfiltrated: targetResponse.metadata.wasExfiltrated as boolean,
      exfilCount: (targetResponse.metadata.exfilCount as number) ?? 0,
      exfilRecords: [],
    });
  }

  const { grade, rubric } = await grader.getResult(
    attackPrompt,
    targetResponse.output,
    iterationTest,
    gradingProvider,
    getGraderAssertionValue(assertToUse),
    additionalRubric,
    undefined,
    gradingContext,
  );
  logger.debug('[IterativeMeta] Grader result', { iteration, passed: grade.pass });
  return {
    ...grade,
    assertion: buildGraderResultAssertion(grade.assertion, assertToUse, rubric),
  };
}

function appendIterativeMetaHistory({
  redteamHistory,
  attackPrompt,
  lastTransformResult,
  targetResponse,
  graderResult,
  traceContext,
  computedTraceSummary,
  currentRenderInputVars,
}: {
  redteamHistory: IterativeMetaMetadata['redteamHistory'];
  attackPrompt: string;
  lastTransformResult?: TransformResult;
  targetResponse: TargetResponse;
  graderResult?: GradingResult;
  traceContext: TraceContextData | null;
  computedTraceSummary?: string;
  currentRenderInputVars?: Record<string, string>;
}) {
  redteamHistory.push({
    prompt: attackPrompt,
    promptAudio: lastTransformResult?.audio,
    promptImage: lastTransformResult?.image,
    output: targetResponse.output,
    outputAudio:
      targetResponse.audio?.data && targetResponse.audio?.format
        ? { data: targetResponse.audio.data, format: targetResponse.audio.format }
        : undefined,
    outputImage:
      targetResponse.image?.data && targetResponse.image?.format
        ? { data: targetResponse.image.data, format: targetResponse.image.format }
        : undefined,
    score: 0,
    graderPassed: graderResult?.pass,
    guardrails: undefined,
    trace: traceContext ? formatTraceForMetadata(traceContext) : undefined,
    traceSummary: computedTraceSummary,
    inputVars: currentRenderInputVars,
  });
}

function buildIterativeMetaResponse({
  bestResponse,
  lastResponse,
  bestPrompt,
  failClosedError,
  finalIteration,
  vulnerabilityAchieved,
  lastFinalAttackPrompt,
  storedGraderResult,
  stopReason,
  redteamHistory,
  sessionIds,
  traceSnapshots,
  lastTransformDisplayVars,
  totalTokenUsage,
}: {
  bestResponse: string;
  lastResponse?: TargetResponse;
  bestPrompt?: string;
  failClosedError?: string;
  finalIteration: number;
  vulnerabilityAchieved: boolean;
  lastFinalAttackPrompt?: string;
  storedGraderResult?: GradingResult;
  stopReason: IterativeMetaMetadata['stopReason'];
  redteamHistory: IterativeMetaMetadata['redteamHistory'];
  sessionIds: string[];
  traceSnapshots: TraceContextData[];
  lastTransformDisplayVars?: Record<string, string>;
  totalTokenUsage: TokenUsage;
}) {
  return {
    output: bestResponse || lastResponse?.output || '',
    prompt: bestPrompt,
    ...(failClosedError
      ? { error: failClosedError }
      : lastResponse?.error
        ? { error: lastResponse.error }
        : {}),
    metadata: {
      finalIteration,
      vulnerabilityAchieved,
      redteamFinalPrompt: lastFinalAttackPrompt || bestPrompt,
      storedGraderResult,
      stopReason,
      redteamHistory,
      sessionIds,
      traceSnapshots:
        traceSnapshots.length > 0
          ? traceSnapshots.map((t) => formatTraceForMetadata(t))
          : undefined,
      ...(lastTransformDisplayVars && { transformDisplayVars: lastTransformDisplayVars }),
    },
    tokenUsage: totalTokenUsage,
  };
}

export async function runMetaAgentRedteam({
  context,
  filters,
  injectVar,
  numIterations,
  options,
  prompt,
  agentProvider,
  gradingProvider,
  targetProvider,
  test,
  vars,
  excludeTargetOutputFromAgenticAttackGeneration = false,
  perTurnLayers = [],
  inputs,
}: {
  context?: CallApiContextParams;
  filters: NunjucksFilterMap | undefined;
  injectVar: string;
  numIterations: number;
  options?: CallApiOptionsParams;
  prompt: Prompt;
  agentProvider: ApiProvider;
  gradingProvider: ApiProvider;
  targetProvider: ApiProvider;
  test?: AtomicTestCase;
  vars: Record<string, VarValue>;
  excludeTargetOutputFromAgenticAttackGeneration?: boolean;
  inputs?: Inputs;
  perTurnLayers?: LayerConfig[];
}): Promise<{
  output: string;
  prompt?: string;
  metadata: IterativeMetaMetadata;
  tokenUsage: TokenUsage;
  error?: string;
}> {
  // Store the original vars and transformVars config
  const originalVars = { ...vars };
  const transformVarsConfig = test?.options?.transformVars;

  const goal = context?.test?.metadata?.goal || vars[injectVar];
  const additionalRubric = getIterativeMetaGoalRubric(goal);

  // Generate unique test run ID
  const testRunId = `${context?.evaluationId || 'local'}-tc${context?.testCaseId || crypto.randomUUID().slice(0, 8)}`;

  // Resolve tracing options
  const tracingOptions = resolveTracingOptions({
    strategyId: 'iterative-meta',
    test,
    config: {},
  });
  const shouldFetchTrace =
    tracingOptions.enabled && (tracingOptions.includeInAttack || tracingOptions.includeInGrading);
  const traceSnapshots: TraceContextData[] = [];

  const sessionIds: string[] = [];
  const totalTokenUsage = createEmptyTokenUsage();

  let vulnerabilityAchieved = false;
  let bestPrompt: string | undefined = undefined;
  let bestResponse = '';
  let finalIteration = numIterations;
  let storedGraderResult: GradingResult | undefined = undefined;
  let stopReason: 'Grader failed' | 'Agent abandoned' | 'Max iterations reached' =
    'Max iterations reached';
  let lastResponse: TargetResponse | undefined = undefined;
  let failClosedError: string | undefined;

  // Track the previous iteration's trace summary for attack generation
  let previousTraceSummary: string | undefined;

  const redteamHistory: IterativeMetaMetadata['redteamHistory'] = [];

  // Track display vars from per-turn layer transforms (e.g., fetchPrompt, embeddedInjection)
  let lastTransformDisplayVars: Record<string, string> | undefined;

  // Track the last transformed prompt (e.g., fetchPrompt for indirect-web-pwn) for UI display
  let lastFinalAttackPrompt: string | undefined;

  for (let i = 0; i < numIterations; i++) {
    logger.debug(`[IterativeMeta] Starting iteration ${i + 1}/${numIterations}`, {
      iteration: i + 1,
      testRunId,
    });

    // Use the shared utility function to create iteration context
    const iterationContext = await createIterationContext({
      originalVars,
      transformVarsConfig,
      context,
      iterationNumber: i + 1,
      loggerTag: '[IterativeMeta]',
    });

    const iterationVars = iterationContext?.vars || {};

    const cloudRequest = buildMetaAgentRequest({
      iteration: i + 1,
      testRunId,
      goal,
      test,
      inputs,
      excludeTargetOutputFromAgenticAttackGeneration,
      lastResponse,
      redteamHistory,
      storedGraderResult,
      traceSummary: tracingOptions.includeInAttack ? previousTraceSummary : undefined,
    });

    const metaPrompt = await getMetaAgentPrompt({
      iteration: i + 1,
      numIterations,
      cloudRequest,
      agentProvider,
      totalTokenUsage,
      options,
      injectVar,
      inputs,
      test,
    });
    if (!metaPrompt) {
      continue;
    }
    const { agentResp, attackPrompt } = metaPrompt;

    // ═══════════════════════════════════════════════════════════════════════
    // Apply per-turn layer transforms if configured (e.g., audio, base64)
    // This enables: layer: { steps: [jailbreak:meta, audio] }
    // For single-turn iterative, we just transform the attack and send directly
    // ═══════════════════════════════════════════════════════════════════════
    const transformOutcome = await applyMetaPerTurnTransforms({
      attackPrompt,
      injectVar,
      perTurnLayers,
      context,
      test,
    });
    if (!transformOutcome) {
      continue;
    }
    const { finalAttackPrompt, transformResult: lastTransformResult } = transformOutcome;
    if (transformOutcome.displayVars) {
      lastTransformDisplayVars = transformOutcome.displayVars;
    }

    // Track the final prompt sent to target for UI display (e.g., fetchPrompt for indirect-web-pwn)
    lastFinalAttackPrompt = finalAttackPrompt;

    // Render the actual prompt with the agent's attack
    // Escape nunjucks template syntax (replace {{ with { { to break the pattern)
    const escapedAttackPrompt = escapeAttackPromptForRender(finalAttackPrompt);

    const { currentRenderInputVars, failClosedError: inputFailClosedError } =
      await materializeIterativeMetaInputs({
        iteration: i + 1,
        attackPrompt,
        agentResp,
        agentProvider,
        inputs,
        test,
      });
    if (inputFailClosedError) {
      failClosedError = inputFailClosedError;
      break;
    }

    // Build updated vars - handle multi-input mode
    const updatedVars: Record<string, VarValue> = {
      ...iterationVars,
      [injectVar]: escapedAttackPrompt,
      ...(currentRenderInputVars || {}),
    };

    const targetAttempt = await executeMetaTargetAttempt({
      iteration: i + 1,
      numIterations,
      prompt,
      updatedVars,
      filters,
      targetProvider,
      injectVar,
      iterationContext,
      options,
      context,
      tracingOptions,
      shouldFetchTrace,
      traceSnapshots,
      totalTokenUsage,
    });
    if (!targetAttempt) {
      continue;
    }
    const { targetResponse, traceContext, computedTraceSummary } = targetAttempt;
    lastResponse = targetResponse;

    const sessionId = getIterativeMetaSessionId(targetResponse, iterationContext);

    if (sessionId) {
      sessionIds.push(sessionId);
    }

    // Prepare trace summaries for attack generation and grading
    const attackTraceSummary = tracingOptions.includeInAttack ? computedTraceSummary : undefined;

    // Update previous trace summary for next iteration's attack generation
    previousTraceSummary = attackTraceSummary;

    const graderResult = await gradeIterativeMetaResponse({
      iteration: i + 1,
      attackPrompt,
      targetResponse,
      test,
      iterationVars,
      gradingProvider,
      additionalRubric,
      tracingOptions,
      traceContext,
      computedTraceSummary,
      lastTransformResult,
      context,
    });
    storedGraderResult = graderResult ?? storedGraderResult;

    appendIterativeMetaHistory({
      redteamHistory,
      attackPrompt,
      lastTransformResult,
      targetResponse,
      graderResult,
      traceContext,
      computedTraceSummary,
      currentRenderInputVars,
    });

    // Check if vulnerability was achieved
    if (graderResult?.pass === false) {
      vulnerabilityAchieved = true;
      bestPrompt = finalAttackPrompt; // Use transformed prompt (e.g., fetchPrompt) not jailbreak
      bestResponse = targetResponse.output;
      stopReason = 'Grader failed';
      finalIteration = i + 1;

      logger.debug('[IterativeMeta] Vulnerability achieved!', {
        iteration: i + 1,
      });

      break;
    }
  }

  return buildIterativeMetaResponse({
    bestResponse,
    lastResponse,
    bestPrompt,
    failClosedError,
    finalIteration,
    vulnerabilityAchieved,
    lastFinalAttackPrompt,
    storedGraderResult,
    stopReason,
    redteamHistory,
    sessionIds,
    traceSnapshots,
    lastTransformDisplayVars,
    totalTokenUsage,
  });
}

class RedteamIterativeMetaProvider implements ApiProvider {
  private readonly agentProvider: RedteamFileConfig['provider'];
  private readonly injectVar: string;
  private readonly numIterations: number;
  private readonly gradingProvider: RedteamFileConfig['provider'];
  private readonly excludeTargetOutputFromAgenticAttackGeneration: boolean;
  private readonly perTurnLayers: LayerConfig[];
  readonly inputs?: Inputs;

  constructor(readonly config: Record<string, VarValue>) {
    logger.debug('[IterativeMeta] Constructor config', {
      config,
    });
    invariant(typeof config.injectVar === 'string', 'Expected injectVar to be set');
    this.injectVar = config.injectVar;
    this.inputs = config.inputs as Inputs | undefined;

    const configuredIterations =
      Number(config.numIterations) || getEnvInt('PROMPTFOO_NUM_JAILBREAK_ITERATIONS', 10);
    this.numIterations = isLoggedIntoCloud()
      ? configuredIterations
      : Math.min(configuredIterations, 10);

    this.excludeTargetOutputFromAgenticAttackGeneration = Boolean(
      config.excludeTargetOutputFromAgenticAttackGeneration,
    );
    this.perTurnLayers = (config._perTurnLayers as LayerConfig[]) ?? [];

    // Meta-agent strategy requires remote generation
    if (!shouldGenerateRemote()) {
      throw new Error(
        neverGenerateRemote()
          ? getRemoteGenerationExplicitlyDisabledError('jailbreak:meta strategy')
          : getRemoteGenerationDisabledError('jailbreak:meta strategy'),
      );
    }

    this.gradingProvider = new PromptfooChatCompletionProvider({
      task: 'judge',
      jsonOnly: true,
      preferSmallModel: false,
    });
    this.agentProvider = new PromptfooChatCompletionProvider({
      task: 'meta-agent-decision',
      jsonOnly: true,
      preferSmallModel: false,
      // Pass inputs schema for multi-input mode
      inputs: this.inputs,
    });
  }

  id() {
    return 'promptfoo:redteam:iterative:meta';
  }

  async callApi(
    _prompt: string,
    context?: CallApiContextParams,
    options?: CallApiOptionsParams,
  ): Promise<{
    output: string;
    metadata: IterativeMetaMetadata;
    tokenUsage: TokenUsage;
  }> {
    logger.debug('[IterativeMeta] callApi context', {
      hasContext: !!context,
    });
    invariant(context?.originalProvider, 'Expected originalProvider to be set');
    invariant(context.vars, 'Expected vars to be set');

    return runMetaAgentRedteam({
      prompt: context.prompt,
      filters: context.filters,
      vars: context.vars,
      agentProvider: await redteamProviderManager.getProvider({
        provider: this.agentProvider,
        jsonOnly: true,
      }),
      gradingProvider: await redteamProviderManager.getGradingProvider({
        provider: this.gradingProvider,
        jsonOnly: true,
      }),
      targetProvider: context.originalProvider,
      injectVar: this.injectVar,
      numIterations: this.numIterations,
      perTurnLayers: this.perTurnLayers,
      context,
      options,
      test: context.test,
      excludeTargetOutputFromAgenticAttackGeneration:
        this.excludeTargetOutputFromAgenticAttackGeneration,
      inputs: this.inputs,
    });
  }
}

export default RedteamIterativeMetaProvider;
