export const COMMON_LANGUAGE_NAMES = LANGUAGES.map((lang) => lang.name).sort();

/**
 * Map of ISO 639-1 codes to language names (lowercase code -> name)
 * Only includes the 184 languages that have ISO 639-1 codes
 */
export const ISO_6391_TO_NAME = new Map<string, string>(
  LANGUAGES.filter((lang) => lang.iso6391).map((lang) => [lang.iso6391!.toLowerCase(), lang.name]),
);

/**
 * Map of ISO 639-2 codes to language names (lowercase code -> name)
 * Includes all 486 languages with ISO 639-2/T (terminological) codes
 */
export const ISO_6392_TO_NAME = new Map<string, string>(
  LANGUAGES.map((lang) => [lang.iso6392.toLowerCase(), lang.name]),
);

/**
 * Map of ISO 639-2/B (bibliographic) codes to language names
 * Only includes the 20 languages that have different B codes
 */
export const ISO_6392B_TO_NAME = new Map<string, string>(
  LANGUAGES.filter((lang) => lang.iso6392b).map((lang) => [
    lang.iso6392b!.toLowerCase(),
    lang.name,
  ]),
);

/**
 * Normalize language input - converts ISO codes to full language names
 * Supports ISO 639-1 (2-letter), ISO 639-2/T (3-letter), and ISO 639-2/B (3-letter bibliographic)
 * @param input - Language name or ISO code (case insensitive)
 * @returns Full language name or original input if not found
 */
export function normalizeLanguage(input: string): string {
  const normalized = input.trim();
  const lowerInput = normalized.toLowerCase();

  // Check if it's an ISO 639-1 code (2 letters)
  if (lowerInput.length === 2 && ISO_6391_TO_NAME.has(lowerInput)) {
    return ISO_6391_TO_NAME.get(lowerInput)!;
  }

  // Check if it's an ISO 639-2/T code (3 letters)
  if (lowerInput.length === 3 && ISO_6392_TO_NAME.has(lowerInput)) {
    return ISO_6392_TO_NAME.get(lowerInput)!;
  }

  // Check if it's an ISO 639-2/B code (3 letters bibliographic)
  if (lowerInput.length === 3 && ISO_6392B_TO_NAME.has(lowerInput)) {
    return ISO_6392B_TO_NAME.get(lowerInput)!;
  }

  // Return original input if not an ISO code
  return normalized;
}
